const branchId = (choiceId: string): BranchId | null => {
  if (choiceId === 'choice-a' || choiceId === 'path_a') return 'a'
  if (choiceId === 'choice-b' || choiceId === 'path_b') return 'b'
  return null
}

export const addWorldClosing = (
  context: NormalizedStoryContext,
  choiceId: string,
  story: string,
): string => {
  if (context.language !== 'ru' || context.ageGroup !== '5-7') return story
  const id = branchId(choiceId)
  return id ? `${story}\n\n${closings[context.stylePackId][id]}` : story
}
