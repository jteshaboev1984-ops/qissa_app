const branchIdFromChoice = (choiceId: string): BranchId | null => {
  if (choiceId === 'choice-a' || choiceId === 'path_a') return 'a'
  if (choiceId === 'choice-b' || choiceId === 'path_b') return 'b'
  return null
}

const isQualitySlice = (context: NormalizedStoryContext) =>
  context.language === 'ru' && context.ageGroup === '5-7'

export const getWorldNarrative = (context: NormalizedStoryContext): WorldNarrative | null =>
  isQualitySlice(context) ? narratives[context.stylePackId] : null

export const getWorldBranchNarrative = (
  context: NormalizedStoryContext,
  choiceId: string,
): WorldBranchNarrative | null => {
  if (!isQualitySlice(context)) return null
  const branchId = branchIdFromChoice(choiceId)
  return branchId ? narratives[context.stylePackId].branches[branchId] : null
}
